                        <td>
                            <p style="font-family: sans-serif; font-size: 18px;">We are back with another exciting update!</p>
                            <p style="font-family: sans-serif; font-size: 18px;">Today, we released the source code for our Mobile App (iOS and Android) under the GPL-v3.0 license. A small gift from us to the planet.</p>
                            <ul style="font-family: sans-serif; font-size: 19px;">
                                <li>
                                    <p>Here is a link to the mobile app GitHub repository where the code lives:</p>
                                    <a href="https://github.com/OpenLitterMap/react-native" target="_blank">https://github.com/OpenLitterMap/react-native</a>
                                    <br><br>
                                    Anyone can now view, inspect and contribute to the OpenLitterMap Web + Mobile app code, or use our code for their own open source projects.
                                </li>
                                <br>
                                <li>
                                    We also released a new feature called OpenLitterMap Teams.
                                    <br><br>
                                    You can now create or join a team and log your impact as a group!
                                </li>
                                <br>
                                <li>
                                    You can join any number of teams, but you can only have 1 active team. When you upload, you make a contribution to your active team. Keep the <a href="https://openlittermap.com/global">Global Map</a> open to see a live update of what teams are contributing!
                                </li>
                                <br>
                                <li>
                                    The Teams feature was used by our first corporate sponsor!
                                    <br><br>
                                    <a href="https://elfc.com/" target="_blank">Engine Lease Finance</a> is an aircraft engine leasing company Headquartered in Ireland who gave their global workforce a half-day to pick up litter and record their positive environmental impact with their families. Huge win!
                                </li>
                                <br>
                                <li>
                                    We made a presentation a high level Geneva Forum citizen science conference at the United Nations
                                    <br><br>
                                    <a href="https://www.youtube.com/watch?v=QhLsA0WIfTA/" target="_blank">You can watch it here</a>.
                                </li>
                                <br>
                                <li>
                                    Since we released the source code for our web-app last update, we have had several people fix bugs and add new features. These include:
                                    <br><br>
                                    - Smooth zooming on the global map
                                    <br><br>
                                    - Click on global map cluster to zoom in
                                    <br><br>
                                    - Progress on the tool that will help us develop the OpenLitterAI
                                    <br><br>
                                    - Missing items in the download
                                    <br><br>
                                    - Fixed missing email verification notification for new users
                                    <br><br>
                                    - and more coding stuff!
                                </li>
                                <br>
                                <li>
                                    We have started using a new feature on github called <a href="https://github.com/OpenLitterMap/openlittermap-web/discussions" target="_blank">Discussions</a>. We would love if you could sign up and chat with us about the future of OpenLitterMap.
                                </li>
                                <br>
                                <li>
                                    As a reminder, we have an active Slack channel where we discuss the future of OpenLitterMap. We would love if you could join us? <a href="https://join.slack.com/t/openlittermap/shared_invite/zt-fdctasud-mu~OBQKReRdC9Ai9KgGROw" target="_blank">Click here to join the chat</a>.
                                </li>
                                <br>
                                <li>
                                    Stay tuned for lots more exiting updates as we empower more people with the tools to share data on pollution.
