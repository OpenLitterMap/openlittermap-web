                        <h2 style="color: #1a1a2e; font-size: 22px; margin: 30px 0 20px 0; border-bottom: 2px solid #2ecc71; padding-bottom: 8px;">&#127466;&#127482; Ireland's EU Presidency</h2>

                        <p>Ireland holds the EU Council Presidency from July to December 2026. This happens roughly once every 14 years. It's a window where Ireland is expected to demonstrate leadership &mdash; and it matters for citizen science.</p>

                        <p>During the Presidency Ireland will try to bring in Digital IDs for social media. The internet has problems, and children are at risk. This is a conversation worth having, but it is not clear if Ireland will do anything to support the development of citizen science as a better use of technology than social media and help people develop skills to overcome the harms of the internet with digital skills training instead of using this as an excuse to introduce orwellian surveillance programmes. Some of the architects of this policy have never built anything.</p>

                        <p>When Germany held the Presidency in 2020, they put &euro;2 million behind <strong>Plastic Pirates Go Europe</strong> &mdash; a citizen science project for schools. Plastic Pirates collected 93,718 items of litter. That's &euro;21.34 per item.</p>

                        <p>OpenLitterMap has collected 850,000+ items with &euro;100K of my personal investment. That's <strong>&euro;0.12 per item. 178&times; more cost-efficient.</strong> but has not qualified for any research or innovation pathway.</p>

                        <p>Ireland has no pathway for citizen science, open source, or independent founders like me. Your contributions are the evidence that this works. The question is whether Irish institutions will recognise what one of their own citizens has built - which is more than the rest of our institutions combined.</p>

                        <p>To celebrate Ireland's EU Presidency, I have prepared the following documents:</p>

                        <p>&#128196; <strong><a href="https://litterweek.org/tdg" target="_blank">The Democracy Gap</a></strong> &mdash; On January 1st 2026, to mark the beginning of the year that Ireland will host the EU Presidency, I personally submitted this policy dossier to OECD &amp; UNESCO headquarters in Paris to document the structural gap between institutional practice and public participation.</p>

                        <p>&#128269; <strong><a href="https://drive.google.com/file/d/1RZ3gowZrt4cNLWozY0iNMBwlZq037zh1/view?usp=sharing" target="_blank">MAPS: Mapping Audit of Public Science</a></strong> &mdash; MAPS is a Freedom of Information request I have submitted to Irish government departments to audit what they have achieved for public scientific inclusion since the launch of the iPhone.</p>

