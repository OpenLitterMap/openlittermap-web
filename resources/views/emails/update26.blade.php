                        <h2 style="color: #1a1a2e; font-size: 22px; margin: 30px 0 20px 0; border-bottom: 2px solid #2ecc71; padding-bottom: 8px;">&#128230; All Tags Migrated</h2>

                        <p>All original tags have been updated to use the new Tagging system. This took <strong>months</strong> of work. 89% of brands were included in the migration. A new time-series metrics database powers every stat on the platform.</p>

                        <!-- LitterWeek -->
                        <h2 style="color: #1a1a2e; font-size: 22px; margin: 30px 0 20px 0; border-bottom: 2px solid #2ecc71; padding-bottom: 8px;">&#127979; LitterWeek</h2>

                        <p><strong><a href="https://litterweek.org" target="_blank">LitterWeek.org</a></strong> is a 5-day digital skills programme for schools. One hour a day. Any week. Students learn about data, privacy, and citizen science &mdash; then go outside, document litter in their community, and put their findings on the global map.</p>

                        <p><strong>4 schools have signed up.</strong> First pilots are running this spring in Cork, Ireland.</p>

                        <p><strong>We're looking for more.</strong> If you know a teacher, a TY coordinator, or a Green Schools contact &mdash; send them to <strong><a href="https://litterweek.org" target="_blank">litterweek.org</a></strong> or <strong><a href="mailto:sean@litterweek.org">sean@litterweek.org</a></strong>.</p>

