                        <h2 style="color: #1a1a2e; font-size: 22px; margin: 30px 0 20px 0; border-bottom: 2px solid #2ecc71; padding-bottom: 8px;">&#10024; What's New</h2>

                        <!-- Feature: Tagging -->
                        <table border="0" cellpadding="0" cellspacing="0" width="100%" style="margin: 0;">
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#127991;&#65039; <strong>New tagging system</strong> &mdash; Previously there was no relationship between tags. Now you can pick an object like &ldquo;Can&rdquo;, then add a type like &ldquo;Beer&rdquo;, attach a brand, set the material(s) &mdash; which are all related to each other. 15 old category tables replaced with one fast, searchable system. There is still a lot of improvements to make, but the new architecture is a huge improvement over the original system that I scrambled together during a dissertation in 2015. The new system also makes it much easier to add new tags. If a tag does not exist you can create new suggestions with Custom Tags by adding text like "brand:newbrand" or "object:newobject" which will get reviewed and integrated into the tagging in a future update.</p>
                            </td></tr>

                            <!-- Feature: Map -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#128506;&#65039; <strong>Improved map &amp; new clustering algorithm</strong> &mdash; When you zoom in and load the points data you will load a new Stats panel that summarises the point data. The points data no longer crashes when loading big data as we now paginate by loading 1,000 points at a time. Clustering has also been completely rewritten from scratch. Previously we had to delete all clusters and re-run the algorithm every 24 hours. The new system updates clusters automatically and it's way faster. In fact it's so good I'm planning to release it as a standalone open source package &mdash; <strong>Clusterize</strong>.</p>
                            </td></tr>

                            <!-- Feature: LitterWeek -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
