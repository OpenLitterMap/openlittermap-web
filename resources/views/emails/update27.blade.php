                        <h2 style="color: #1a1a2e; font-size: 22px; margin: 30px 0 20px 0; border-bottom: 2px solid #2ecc71; padding-bottom: 8px;">&#128241; Updated Mobile Apps</h2>

                        <p>Updated apps for <strong>iOS</strong> and <strong>Android</strong> are now available.</p>

                        <!-- HomeScreen -->
                        <table border="0" cellpadding="0" cellspacing="0" width="100%" style="margin: 0;">
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#127968; <strong>A completely new mobile app experience</strong> &mdash; A live dashboard showing real-time impact alongside the global community. Total tags, photos, and people with animated counters that update on pull-to-refresh. Personal stats, XP, level progress, and streak are all visible at a glance.</p>
                            </td></tr>

                            <!-- Photo workflow -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#128248; <strong>Camera &amp; faster photo-to-tag workflow</strong> &mdash; The in-app camera is back &mdash; take a photo directly from the app. Recent geotagged photos also appear right on the dashboard. The entire flow is now 3 steps: open photo, tag it, submit.</p>
                            </td></tr>

                            <!-- Quick Tags -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#11088; <strong>Quick Tags &mdash; (v7.6.0) save your favourites</strong> &mdash; Tap the star on any tag to save it for faster tagging. Starred tags appear as suggested tags right above the search bar so you don't even have to search. You can give each one a custom name, set a default quantity, and even pre-define materials and brands. Available to edit in settings.</p>
                            </td></tr>

                            <!-- Smarter tagging -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#127991;&#65039; <strong>Smarter tagging</strong> &mdash; Previously there was no relationship between tags. Since OLM v5 we have added a new object-based tagging system. On mobile a new tag detail sheet makes it easy to add materials, brands, and custom tags to any object. Many more improvements still to make.</p>
                            </td></tr>

                            <!-- Onboarding -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#127891; <strong>New onboarding tutorial</strong> &mdash; A new tutorial on both web and mobile walks first-time users through submitting their first data points: take a photo, tag it, upload it, and see it on the map. GPS camera setup instructions are built in.</p>
                            </td></tr>

                            <!-- GPS -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#128205; <strong>GPS fixes for Android</strong> &mdash; Critical GPS bugs resolved which was blocking some Android users from uploading.</p>
                            </td></tr>

                            <!-- Photo permissions -->
                            <tr><td style="padding: 14px 0; border-bottom: 1px solid #eeeeee;">
                                <p style="margin: 0;">&#128247; <strong>Photo permissions</strong> &mdash; Easier to control which photos the app has permission to import.</p>
                            </td></tr>

                            <!-- And more -->
                            <tr><td style="padding: 14px 0;">
                                <p style="margin: 0;">&#128736; <strong>And more</strong> &mdash; New tab bar with cleaner navigation. iPad layout support. Camera preview with GPS coordinates and delete option. Improved update prompt when a new version is available & significant code improvements everywhere.</p>
                            </td></tr>
                        </table>

